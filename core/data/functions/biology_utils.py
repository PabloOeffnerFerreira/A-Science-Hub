def reverse_complement(seq: str) -> str:
    """Return reverse complement of a DNA sequence"""
    return str(Seq(seq).reverse_complement())

def translate_all_frames(seq: str) -> list[str]:
    """Translate DNA sequence in all 3 reading frames"""
    s = Seq(seq)
    return [str(s[frame:].translate(to_stop=False)) for frame in range(3)]

def gc_content_percent(seq: str) -> float:
    """Return GC content percentage of DNA sequence"""
    return gc_fraction(seq) * 100
